    private void tinhTong() {
        DefaultTableModel model = (DefaultTableModel) tblThuChi.getModel();
        double tongThu = 0;
        double tongChi = 0;

        for (int i = 0; i < model.getRowCount(); i++) {
            Object soTienObj = model.getValueAt(i, 2); // C·ªôt s·ªë ti·ªÅn
            Object loaiObj = model.getValueAt(i, 3);   // C·ªôt lo·∫°i: Thu / Chi

            if (soTienObj != null && loaiObj != null) {
                try {
                    double soTien = Double.parseDouble(soTienObj.toString().replace(",", "").trim());
                    String loai = loaiObj.toString().trim();

                    if (loai.equalsIgnoreCase("Thu")) {
                        tongThu += soTien;
                    } else if (loai.equalsIgnoreCase("Chi")) {
                        tongChi += soTien;
                    }
                } catch (NumberFormatException e) {
                    // N·∫øu kh√¥ng ph·∫£i s·ªë, b·ªè qua d√≤ng ƒë√≥
                }
            }
        }

        double loiNhuan = tongThu - tongChi;

        // ƒê·ªãnh d·∫°ng ti·ªÅn t·ªá
        java.text.NumberFormat format = java.text.NumberFormat.getInstance();
        format.setMaximumFractionDigits(0);

        JOptionPane.showMessageDialog(this,
                "üì• T·ªïng Thu: " + format.format(tongThu) + " VNƒê"
                + "\nüì§ T·ªïng Chi: " + format.format(tongChi) + " VNƒê"
                + "\nüí∞ L·ª£i Nhu·∫≠n: " + format.format(loiNhuan) + " VNƒê",
                "T·ªïng h·ª£p thu chi", JOptionPane.INFORMATION_MESSAGE
        );

    }
    

private void luuFileToDocuments() {
        // L·∫•y th∆∞ m·ª•c Documents trong h·ªá th·ªëng
        String userHome = System.getProperty("user.home");
        String documentsFolder = userHome + File.separator + "Documents" ;  // Th∆∞ m·ª•c Documents

        // ƒê·∫£m b·∫£o th∆∞ m·ª•c Documents t·ªìn t·∫°i
        File folder = new File(documentsFolder);
        if (!folder.exists()) {
            folder.mkdirs(); // T·∫°o th∆∞ m·ª•c Documents n·∫øu kh√¥ng t·ªìn t·∫°i
        }

        // ƒê·ªãnh nghƒ©a t√™n file l∆∞u
        String fileName = "data.csv";  // T√™n file l∆∞u

        // T·∫°o file trong th∆∞ m·ª•c Documents
        File fileToSave = new File(documentsFolder, fileName);

        try (BufferedWriter writer = new BufferedWriter(new FileWriter(fileToSave))) {
            DefaultTableModel model = (DefaultTableModel) tblThuChi.getModel();
            int rowCount = model.getRowCount();
            int columnCount = model.getColumnCount();

            // Ghi ti√™u ƒë·ªÅ c·ªôt
            for (int i = 0; i < columnCount; i++) {
                writer.write(model.getColumnName(i));
                if (i < columnCount - 1) {
                    writer.write(",");
                }
            }
            writer.newLine();

            // Ghi d·ªØ li·ªáu c√°c h√†ng
            for (int i = 0; i < rowCount; i++) {
                for (int j = 0; j < columnCount; j++) {
                    Object value = model.getValueAt(i, j);
                    // Ki·ªÉm tra null v√† thay th·∫ø b·∫±ng chu·ªói r·ªóng n·∫øu c·∫ßn
                    writer.write(value != null ? value.toString() : "");
                    if (j < columnCount - 1) {
                        writer.write(",");
                    }
                }
                writer.newLine();
            }

            JOptionPane.showMessageDialog(this, "D·ªØ li·ªáu ƒë√£ ƒë∆∞·ª£c l∆∞u th√†nh c√¥ng t·∫°i " + fileToSave.getAbsolutePath());

        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "C√≥ l·ªói khi l∆∞u file: " + e.getMessage());
        }
    }
    

